public class SixOne {
	
	private int id;//主键ID
	private int qishu; // 期数
	private int frist; // 第一个号码
	private int second;// 第二个号码
	private int third; // 第三
	private int fourth;// 第四
	private int fifth;// 第五
	private int sixth;// 第六
	private int seventh;// 第七 号码
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getQishu() {
		return qishu;
	}
	public void setQishu(int qishu) {
		this.qishu = qishu;
	}
	public int getFrist() {
		return frist;
	}
	public void setFrist(int frist) {
		this.frist = frist;
	}
	public int getSecond() {
		return second;
	}
	public void setSecond(int second) {
		this.second = second;
	}
	public int getThird() {
		return third;
	}
	public void setThird(int third) {
		this.third = third;
	}
	public int getFourth() {
		return fourth;
	}
	public void setFourth(int fourth) {
		this.fourth = fourth;
	}
	public int getFifth() {
		return fifth;
	}
	public void setFifth(int fifth) {
		this.fifth = fifth;
	}
	public int getSixth() {
		return sixth;
	}
	public void setSixth(int sixth) {
		this.sixth = sixth;
	}
	public int getSeventh() {
		return seventh;
	}
	public void setSeventh(int seventh) {
		this.seventh = seventh;
	}
	
}
